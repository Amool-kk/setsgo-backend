import mongoose, { Schema } from "mongoose";

const passTokenSchema = new Schema({
  userId: {
    type: Schema.Types.ObjectId,
    required: true,
    ref: "User",
  },
  token: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
    expires: 3600,
  },
});

export const PasswordToken = mongoose.model("passToken", passTokenSchema);
